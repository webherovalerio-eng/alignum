/**
 * Geteilte deterministische RNG-Hilfen für Spintax und Display-Title-Picker.
 * Externalisiert aus spintax.ts, damit andere Module dieselbe Seed-Logik
 * nutzen können, ohne den Spin-Parser zu duplizieren.
 */

export function hash(s: string) {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

export function mulberry32(seed: number) {
  return () => {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
